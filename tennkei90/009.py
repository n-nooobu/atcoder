import cmath
from bisect import *

N = int(input())
XY = [list(map(int, input().split())) for _ in range(N)]

pi = cmath.pi
ans = 0
for i in range(N):
    arg = []
    for j in range(N):
        if i == j:
            continue
        z = XY[j][0] - XY[i][0] + 1j * (XY[j][1] - XY[i][1])
        arg.append(cmath.phase(z))
    arg = sorted(arg)
    for j in range(N - 1):
        idx_p = bisect_left(arg, arg[j] + pi)
        for idx in [idx_p, idx_p - 1]:
            if idx == N - 1:
                continue
            if arg[idx] - arg[j] > pi:
                ans = max(ans, arg[j] + 2 * pi - arg[idx])
            else:
                ans = max(ans, arg[idx] - arg[j])

print(ans * 180 / pi)
